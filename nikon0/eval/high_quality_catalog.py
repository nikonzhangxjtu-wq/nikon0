from __future__ import annotations

from pathlib import Path
from typing import Any

from nikon0.eval.agent_dataset import AgentEvalCase, AgentEvalTurn, ExpectedOutcome


def build_high_quality_agent_dataset(
    *,
    manual_dir: str | Path = "/Users/nikonzhang/compeletion/手册",
    target_size: int = 150,
) -> list[AgentEvalCase]:
    manual_path = Path(manual_dir)
    cases = []
    cases.extend(_product_support_cases(manual_path))
    cases.extend(_case_intake_cases())
    cases.extend(_refund_cases())
    cases.extend(_handoff_cases())
    cases.extend(_composite_cases())
    cases.extend(_boundary_cases())
    _assert_unique_case_ids(cases)
    if len(cases) != 150:
        raise ValueError(f"expected 150 curated cases, got {len(cases)}")
    if target_size > len(cases):
        raise ValueError(f"target_size={target_size} exceeds curated pool size={len(cases)}")
    return cases[:target_size]


def _case(
    *,
    case_id: str,
    category: str,
    message: str = "",
    turns: list[str] | None = None,
    golden_answer: str,
    acceptable_skills: list[str | None],
    required_tools: list[str] | None = None,
    risk_level: str = "low",
    approval_required: bool = False,
    handoff_required: bool = False,
    min_evidence_count: int = 0,
    answer_must_contain: list[str] | None = None,
    answer_must_not_contain: list[str] | None = None,
    metadata: dict[str, Any] | None = None,
    notes: str = "",
) -> AgentEvalCase:
    turn_models = [AgentEvalTurn(message=item) for item in (turns or [])]
    return AgentEvalCase(
        case_id=case_id,
        category=category,  # type: ignore[arg-type]
        message=message or (turn_models[0].message if turn_models else ""),
        turns=turn_models,
        golden_answer=golden_answer,
        expected=ExpectedOutcome(
            acceptable_skills=acceptable_skills,
            required_tools=required_tools or [],
            risk_level=risk_level,
            approval_required=approval_required,
            handoff_required=handoff_required,
            min_evidence_count=min_evidence_count,
            answer_must_contain=answer_must_contain or [],
            answer_must_not_contain=answer_must_not_contain or [],
        ),
        metadata=metadata or {},
        notes=notes,
    )


