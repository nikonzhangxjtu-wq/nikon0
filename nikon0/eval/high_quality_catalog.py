def _assert_unique_case_ids(cases: list[AgentEvalCase]) -> None:
    seen: set[str] = set()
    duplicates: list[str] = []
    for case in cases:
        if case.case_id in seen:
            duplicates.append(case.case_id)
        seen.add(case.case_id)
    if duplicates:
        raise ValueError(f"duplicate eval case ids: {', '.join(sorted(duplicates))}")
